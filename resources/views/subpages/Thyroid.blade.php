                <p style="text-align:justify;">The most common form of thyroid disease is hypothyroidism, which occurs when the thyroid does not produce enough of the hormones triiodothyronine (T3) and thyroxine (T4).
                </p>

                <p style="text-align:justify;">This can lead to a number of health issues, including fatigue, weight gain, depression, and difficulty concentrating. Hypothyroidism can also cause a decrease in bone density, muscle weakness, and an increased risk of heart disease.
                </p>

                <p style="text-align:justify;">Hyperthyroidism, on the other hand, is a condition in which the thyroid produces too much of the hormones T3 and T4. This can cause symptoms such as anxiety, palpitations, weight loss, and irritability. Hyperthyroidism can also cause an irregular heartbeat, tremors, and an enlarged thyroid gland.
                </p>


