            </div>
            <div class="about-content">
                <p style="text-align:justify;">The primary function of your kidneys is to eliminate waste, and control the levels of calcium, potassium, and sodium in your urine. Calcium stones which occur in the form of calcium phosphate are more common in metabolic diseases like renal tubular acidosis; and with taking certain vaccinations like topiramate (Topamax) used to treat migraine headaches or seizure. A high sodium diet increases the amount of calcium to be filtered by your kidneys, which increases the chances of kidney stones. Drinking too little water is the biggest cause of the formation of kidney stones. It causes retention of dissolved minerals, and prevents elimination of uric acids and other wastes. The latter crystallizes to form uric acid stones. Too much uric acid could be caused by a diet that is high in animal protein and low in fruits and vegetables; type 2 diabetes i.e. high blood sugar; gout; being overweight; and chronic diarrhea.
